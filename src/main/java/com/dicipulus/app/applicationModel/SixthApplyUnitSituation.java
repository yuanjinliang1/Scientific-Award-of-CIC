package com.dicipulus.app.applicationModel;
/*
 * 科技进步类、技术发明类
 * 应用单位情况表
 */
public class SixthApplyUnitSituation {
	int idOfApplyUnit;
	private String unitName;
	private String applyTechnology;
	private String startDate;
	private String contactAndPhoneNumber;
	private String applySituation;
	
	
	public int getIdOfApplyUnit() {
		return idOfApplyUnit;
	}
	public void setIdOfApplyUnit(int idOfApplyUnit) {
		this.idOfApplyUnit = idOfApplyUnit;
	}
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	public String getApplyTechnology() {
		return applyTechnology;
	}
	public void setApplyTechnology(String applyTechnology) {
		this.applyTechnology = applyTechnology;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getContactAndPhoneNumber() {
		return contactAndPhoneNumber;
	}
	public void setContactAndPhoneNumber(String contactAndPhoneNumber) {
		this.contactAndPhoneNumber = contactAndPhoneNumber;
	}
	public String getApplySituation() {
		return applySituation;
	}
	public void setApplySituation(String applySituation) {
		this.applySituation = applySituation;
	}
	
}
