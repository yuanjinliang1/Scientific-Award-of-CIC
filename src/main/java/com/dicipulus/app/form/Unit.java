public class Unit {
	String unitName;
	String applyTechnique;
	String startTame;
	String contacts;
	String phoneNumber;
	String information;
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	public String getApplyTechnique() {
		return applyTechnique;
	}
	public void setApplyTechnique(String applyTechnique) {
		this.applyTechnique = applyTechnique;
	}
	public String getStartTame() {
		return startTame;
	}
	public void setStartTame(String startTame) {
		this.startTame = startTame;
	}
	public String getContacts() {
		return contacts;
	}
	public void setContacts(String contacts) {
		this.contacts = contacts;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getInformation() {
		return information;
	}
	public void setInformation(String information) {
		this.information = information;
	}
	
}
